const mongoose = require('mongoose')

const privateUserSchema = new mongoose.Schema({
    firstName: {
        type: String,
        required: true
    },
    lastName: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    isAdmin: {
        type: Boolean,
        required: true
    }
})

const PrivateUser = mongoose.model('PrivateUser', privateUserSchema)

module.exports = PrivateUser